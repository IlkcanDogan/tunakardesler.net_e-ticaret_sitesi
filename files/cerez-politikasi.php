									<br />
									Bir&ccedil;ok site gibi, Sitemiz&#39;i, Hizmetlerimiz&#39;i, uygulamalarımızı&nbsp;ve ara&ccedil;larımızı ziyaret ettiğinizde veya bunlar ile etkileşimde bulunduğunuzda birtakım bilgileri kaydetmemizi ve sizi ayırt etmemizi sağlayan, bilgisayar, tablet, mobil telefon ve diğer cihazlarınıza (birlikte, &quot;cihaz&quot; olarak adlandırılacaktır) yerleştirilen ufak veri dosyaları niteliğinden ibaret olan teknolojiler kullanmaktayız.<br />
									<br />
									Hizmetlerimizi, reklamlarımızı veya e-postalarımızı kullanımınız kapsamında; g&ouml;r&uuml;nt&uuml;lediğiniz sayfalar, tıkladığınız bağlantılar ve ger&ccedil;ekleştirdiğiniz diğer işlemler gibi konularda bilgi toplamak i&ccedil;in kullandığımız &ccedil;erez (cookie), web işaret&ccedil;ileri (web beacons) ve diğer benzer teknolojilerin isimleri ve t&uuml;rleri zaman zaman değişebilmektedir. Bu nedenle &Ccedil;erez Politikasının ve kullandığımız teknolojilerin daha iyi anlaşılmasına yardımcı olması i&ccedil;in belli başlı terminoloji ve tanımlara aşağıda yer verilmektedir:<br />
									<br />
									<strong>&Ccedil;erezler (Cookies) -</strong>&nbsp;Herhangi bir internet sitesini ziyaret ettiğinizde veya herhangi bir iletiyi g&ouml;r&uuml;nt&uuml;lediğinizde tarayıcı veya cihazınızın hafızasına yerleştirilen, genellikle harf ve rakamlardan oluşan kısa metin dosyalarıdır. &Ccedil;erezlerin birka&ccedil; t&uuml;r&uuml; bulunmaktadır:<br />
									<br />
									Oturum &ccedil;erezleri, tarayıcı oturumunuz sonlandığında sona eren ve s&ouml;z konusu tarayıcı oturumunuz esnasındaki işlemlerinizi takip etmemize imk&acirc;n tanıyan &ccedil;erezlerdir.<br />
									<br />
									Kalıcı &ccedil;erezler, tarayıcı oturumları arasında cihazınızda saklanan ve bize tercihlerinizi hatırlama imk&acirc;nı tanıyan &ccedil;erezlerdir.<br />
									<br />
									Birinci taraf &ccedil;erezleri, ziyaret ettiğiniz site tarafından tanımlanan &ccedil;erezlerdir.<br />
									<br />
									&Uuml;&ccedil;&uuml;nc&uuml; taraf &ccedil;erezleri, ziyaret ettiğiniz site dışındaki &uuml;&ccedil;&uuml;nc&uuml; kişi site tarafından tanımlanan &ccedil;erezlerdir.<br />
									<br />
									&Ccedil;erezler &ccedil;oğu tarayıcıda bulunan ara&ccedil;lar ile devre dışı bırakılabilir veya kaldırılabilir. Tercihlerinizin, kullandığınız her bir tarayıcı i&ccedil;in ayrı olarak tanımlanması gerekmekte olup farklı tarayıcılar, farklı işlevler ve opsiyonlar sunmaktadır.<br />
									<br />
									<strong>Web işaret&ccedil;ileri (Web Beacons)</strong><br />
									Sitemiz, Hizmetlerimiz, uygulamalarımız ve ara&ccedil;larımıza dahil edilebilen, genellikle &ccedil;erezler ile birlikte &ccedil;alışan, kullanıcılarımız ile kullanıcı davranışlarını tespit etmeye yarayan ve &quot;piksel etiketleri&quot; veya &quot;clear GIFler&quot; olarak da bilinen ufak kısa grafik imgelerdir.<br />
									<br />
									<strong>Benzer teknolojiler</strong><br />
									Yerel paylaşımlı nesne veya yerel depolama kullanarak tarayıcı veya cihazınızda bilgi saklayan flash &ccedil;erezler, HTML 5 &ccedil;erezler ve diğer internet uygulamaları, yazılım y&ouml;ntemleri gibi teknolojilerdir. Bu teknolojiler, t&uuml;m tarayıcılarınız &uuml;zerinden &ccedil;alışabilir. Bazı hallerde, bunları t&uuml;m&uuml;yle tarayıcınız aracılığıyla kontrol edemeyebilir ve bu t&uuml;r durumlarda doğrudan doğruya y&uuml;klenen uygulamalarınız veya cihazınız aracılığıyla kontrol sağlamanız gerekebilir. Bu teknolojileri, Sitemiz&#39;de veya Sitemiz dışında size y&ouml;nelik hedefli reklamcılık yapmak amacıyla bilgi toplamak i&ccedil;in kullanmamaktayız.<br />
									<br />
									Birbirleri yerine de kullanılabilen &quot;&ccedil;erezler&quot; veya &quot;benzer teknolojiler&quot; terimlerini, Site&#39;mize ait t&uuml;m politika metinlerinde, tarayıcınızda veya cihazınızda veri saklamak i&ccedil;in kullandığımız ve yukarıda belirtilen şekilde bilgi toplayan veya sizi teşhis etmemize yardımcı olan t&uuml;m teknolojileri ifade etmek &uuml;zere kullanabilmekteyiz.<br />
									<br />
									<strong>Sizin se&ccedil;iminiz ve &ccedil;erez, web işaret&ccedil;ileri ve benzer teknolojiler kullanımımız</strong><br />
									Yalnızca bu teknolojiler aracılığıyla kullanılabilir olan birtakım Site &ouml;zellikleri, hizmetleri, uygulamaları sunmaktayız. Bu teknolojileri, tarayıcınızın, y&uuml;klediğiniz uygulamanın veya cihazınızın m&uuml;saade etmesi halinde, her zaman engelleyebilir, silebilir veya devre dışı bırakabilirsiniz. Ancak, &ccedil;erezler ve diğer benzeri teknolojilerin kullanımını devre dışı bırakmanız halinde, birtakım Site &ouml;zelliklerinden, Hizmetler&#39;den, uygulamalardan ve ara&ccedil;lardan faydalanamayabilirsiniz. Ayrıca, tarayıcı oturumunuz esnasında şifrenizi daha sık girmeniz gerekebilir. Bu teknolojileri nasıl engelleyeceğiniz, sileceğiniz veya devre dışı bırakacağınız hakkında daha ayrıntılı bilgi i&ccedil;in, l&uuml;tfen tarayıcı ve cihaz ayarlarınızı g&ouml;zden ge&ccedil;iriniz.<br />
									<br />
									Genel olarak bu teknolojiler, tarayıcınız veya cihazınıza, Sitemiz, Hizmetlerimiz, uygulamalarımız ve ara&ccedil;larımız tarafından ileride sunucumuzun veya d&acirc;hili sistemlerimizin sizi tanımasına yarayacak bilgilerin saklamasını sağlayan teknolojilerdir. Uygulanabilir olduğu durumlarda, &ccedil;erezlerimizi ve diğer benzeri teknolojilerimizi, sadece bizim ve/veya yetkili hizmet sağlayıcılarımızın okuyabilmesini veya takip edebilmesini temin etmek amacıyla sadece bizlerin okuyabileceği şekilde tasarlanmış benzersiz ve tek bir tanımlayıcı atayarak (a unique identifier) koruyoruz. Bizim tarafımızdan eklenen &ccedil;erez veya benzer teknolojilerimizde kişisel verilerinizi depolamamaktayız.<br />
									<br />
									Kişisel verileriniz bu teknolojiler kullanılarak ancak bilgilendirmede bulunarak ve ilgili mevzuatta d&uuml;zenlenen bir hukuki sebebe dayanarak toplanmakta ve depolanmaktadır..<br />
									<br />
									Bu teknolojileri aşağıdaki genel kategoriler kapsamında kullanmaktayız:<br />
									<br />
									<strong>Operasyonel Olarak Gerekli Olanlar</strong><br />
									Sitemiz&#39;in, Hizmetlerimiz&#39;in, uygulamalarımızın ve ara&ccedil;larımızın işlerliğini sağlamak i&ccedil;in gerekli olan &ccedil;erezler, web işaret&ccedil;ileri ve diğer benzer teknolojileri kullanabiliriz. Bunlar, Sitemiz&#39;e, hizmetlerimize, uygulamalarımıza ve ara&ccedil;larımıza erişiminizi sağlayan, kural ve usullere uygun olmayan site davranışlarını tespit etmek, hileli faaliyetleri engellemek ve g&uuml;venliği iyileştirmek i&ccedil;in gerekli olan veya alışveriş sepetleri, aramaları kaydetme veya benzer işlevleri kullanmanıza imk&acirc;n tanıyan teknolojileri i&ccedil;ermektedir.<br />
									<br />
									<strong>Performansla İlgili Olanlar.</strong><br />
