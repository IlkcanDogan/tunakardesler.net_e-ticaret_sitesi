									Sitemiz&#39;e, Hizmetlerimiz&#39;e, uygulamalarımıza ve ara&ccedil;larımıza erişim sağladığınızda veya bunları kullandığınızda size daha gelişmiş &ouml;zellikler sunmamıza imk&acirc;n tanıyan &ccedil;erezler, web işaret&ccedil;ileri ve diğer benzer teknolojiler kullanabiliriz. Bu &ouml;zellikler arasında, Sitemiz&#39;e giriş yaptığınızda sizi tanımamız ya da belli olan tercihleriniz ve ilgi alanlarınızı veya ge&ccedil;mişte g&ouml;r&uuml;nt&uuml;lediğiniz &uuml;r&uuml;nleri hafızada tutmamız gibi Sitemiz&#39;de sunduğumuz i&ccedil;eriğin iyileştirebilmesini sağlayan &ouml;zellikler yer almaktadır;<br />
									<br />
